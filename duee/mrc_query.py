def gen_query(event_type, argument_type):
    # Event to Query
    key = f'{event_type}-{argument_type}'
    if argument_type == '时间':
        query = f'找到{event_type}{argument_query_map[key]}'
    elif '？' in argument_query_map[key]:
        query = argument_query_map[key]
    elif argument_type == argument_query_map[key]:
        query = f'找到{event_type}事件中的{argument_type}'
    else:
        query = f'找到{event_type}事件中的{argument_type},包括{argument_query_map[key]}'
    return query


